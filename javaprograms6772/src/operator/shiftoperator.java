package operator;

public class shiftoperator {

	public static void main(String[] args)
	{
		int a=34;
		int b=-20;
		System.out.println("Signed Right shift 34 devide by 2 ="+(a>>1));
		System.out.println("Signed Right shift -20 devide by 2 ="+(b>>1));
		System.out.println("Signed left shift 34 devide by 2 ="+(a<<1));
		System.out.println("Signed left shift -20 devide by 2 ="+(a<<1));
		System.out.println("unSigned Right shift 34 ="+(a>>>1));
		System.out.println("unSigned Right shift -20="+(b>>>1));
		
		
		
	}

}
