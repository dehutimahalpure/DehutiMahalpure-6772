package operator;

public class incAndDecoperator {

	public static void main(String[] args) 
	{
		int a=10;
		int b=5;
		System.out.println("value of a "+a);
		System.out.println("value of b "+b);
		System.out.println("value of ++a "+(++a));
		System.out.println("value of a "+a);
		System.out.println("value of b++ "+(b++));
		System.out.println("value of b "+b);
	
   

}
}
